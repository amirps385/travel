const emit = defineEmits(['close'])

// Local state
const loading = ref(false)
const logs = ref<any[]>([])
const selectedLog = ref<any>(null)
const showRawHtml = ref(false)
const showFullScreen = ref(false)

// Helper functions
const pretty = (obj: any) => {
  try {
    return JSON.stringify(obj, null, 2)
  } catch {
    return String(obj)
  }
}

const formatDateShort = (iso: string | null | undefined) => {
  if (!iso) return 'â€”'
  try {
    return new Date(iso).toLocaleString(undefined, {
      month: 'short',
      day: 'numeric',
      hour: '2-digit',
      minute: '2-digit'
    })
  } catch {
    return iso
  }
}

const formatDateLong = (iso: string | null | undefined) => {
  if (!iso) return 'â€”'
  try {
    return new Date(iso).toLocaleString(undefined, {
      weekday: 'short',
      year: 'numeric',
      month: 'short',
      day: 'numeric',
      hour: '2-digit',
      minute: '2-digit'
    })
  } catch {
    return iso
  }
}

// NEW: Helper to check if log has full HTML preview
const hasFullHtmlPreview = (log: any) => {
  return (log?.fullHtml && log.fullHtml.length > 100) || (log?.html && log.html.length > 100)
}

// NEW: Helper to get email HTML from either field
const getEmailHtml = (log: any) => {
  return log?.fullHtml || log?.html || ''
}

// Format HTML for iframe rendering - UPDATED TO USE HELPER
