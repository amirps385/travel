}

// Actions
const selectLog = (log: any) => {
  selectedLog.value = log
  showRawHtml.value = false
  
  // Debug
  console.log('Selected Log:', {
    id: log.id,
    hasFullHtml: log.fullHtml && log.fullHtml.length > 100,
    hasHtml: log.html && log.html.length > 100,
    fullHtmlLength: log.fullHtml?.length || 0,
    htmlLength: log.html?.length || 0,
    previewHtml: log.previewHtml
  })
}

const load = async () => {
  if (!props.leadId) return
  
  loading.value = true
  try {
    console.log('ðŸ” Loading email logs for lead:', props.leadId)
    
    // FIRST: Get the lead details to get the email
    let leadEmail = ''
    try {
      const leadDetails: any = await $fetch(`/api/leads/${props.leadId}`)
      leadEmail = leadDetails?.email
      console.log('ðŸ“§ Lead email found:', leadEmail)
    } catch (leadError) {
      console.error('âŒ Failed to fetch lead details:', leadError)
    }
    
    // Try multiple approaches to find email logs
    
    // APPROACH 1: Direct lead endpoint (for lead-specific emails)
    try {
      console.log('ðŸ”„ Trying lead-specific endpoint...')
      const leadResponse = await $fetch(`/api/leads/${props.leadId}/emails`)
      if (Array.isArray(leadResponse) && leadResponse.length > 0) {
        logs.value = leadResponse
        console.log(`âœ… Found ${logs.value.length} emails via lead endpoint`)
        if (logs.value.length > 0) {
          selectLog(logs.value[0])
        }
        loading.value = false
        return
      }
    } catch (error) {
      console.log('ðŸ“­ Lead-specific endpoint returned no emails or failed:', error)
    }
    
    // APPROACH 2: General log endpoint with leadId
    console.log('ðŸ”„ Trying general log endpoint with leadId...')
    const response: any = await $fetch('/api/emailtest/log', {
      params: { 
        leadId: props.leadId,
        limit: 100 // Get more logs
      }
    })
    
    console.log('ðŸ“‹ General log response:', response)
    
    if (response?.success && response?.data?.logs) {
      logs.value = response.data.logs
      console.log(`âœ… Found ${logs.value.length} emails via general endpoint`)
      
      // Debug: check each log
      logs.value.forEach((log, index) => {
        console.log(`ðŸ“ Log ${index + 1}:`, {
          id: log.id,
          leadId: log.leadId,
          hasLeadId: !!log.leadId,
          subject: log.subject,
          templateId: log.templateId
        })
      })
      
      if (logs.value.length > 0) {
        selectLog(logs.value[0])
      }
    } else if (leadEmail) {
      // APPROACH 3: If no logs found by leadId, try by email
      console.log(`ðŸ”„ No logs found by leadId, trying by email: ${leadEmail}`)
      const emailResponse: any = await $fetch('/api/emailtest/log', {
        params: { 
          email: leadEmail,
          limit: 100
        }
      })
      
